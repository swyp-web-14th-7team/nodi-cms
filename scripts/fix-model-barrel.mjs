// orval v8 의 tags-split 모드는 일부 스키마 파일(예: profile-card-templates)을
// model/index.ts 배럴에 export 하지 않고 누락시키는 버그가 있다. 그 결과 생성된
// endpoint 파일이 배럴에서 import 하는 타입을 찾지 못해 빌드가 깨진다.
// 이 스크립트는 model 디렉터리의 모든 파일을 배럴에 다시 채워 넣어 자가 복구한다.
// orval.config.ts 의 hooks.afterAllFilesWrite 에서 매 코드젠 후 실행된다.
import { readdirSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const modelDir = join(here, '..', 'src', 'shared', 'api', 'model')

const files = readdirSync(modelDir)
  .filter((f) => f.endsWith('.ts') && f !== 'index.ts')
  .map((f) => f.replace(/\.ts$/, ''))
  .sort((a, b) => a.localeCompare(b, 'en'))

const header = `/**
 * Generated by orval v8.20.0 🍺
 * Do not edit manually.
 * 프로필 카드 공유 서비스 API
 * 프로필 카드 공유 서비스 백엔드
 * OpenAPI spec version: 0.2.0
 */
`

const body = files.map((name) => `export * from "./${name}";`).join('\n')

writeFileSync(join(modelDir, 'index.ts'), `${header}\n${body}\n`)

console.log(`✅ model barrel 재생성: ${files.length}개 export`)
